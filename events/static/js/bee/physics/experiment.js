class Experiment {

    constructor(name = 'unkown') {
        this.name = name; // name is same as in larsoft
        this.tpc = {
            location: [ // array of TPC locations in cm: [xmin, xmax, ymin, ymax, zmin, zmax]
                [0, 0, 0, 0, 0, 0.]
            ],
            viewAngle: [-60, 60, 0], // angle in degrees w.r.t vertical 
            halfxyz: [0, 0, 0],
            center: [0, 0, 0],
            boxROI: [0, 0, 0, 0, 0, 0], // box of interest, e.g. near the beam
            driftVelocity: 0.16, // cm/us
        };
        this.daq = {
            timeBeforeTrigger: 500 * 0.5, // us
            timeAfterTrigger: 5500 * 0.5, // us        
        };
        this.beam = {
            dir: null,
            center: null,
        };
        this.op = {
            location: {}, // optical detector location {id: [x, y, z]}
            nDet: 0,
            beamTimeMin: 2,  // time window for beam flash, (us) 
            beamTimeMax: 6,
            numiTimeMin: 4,  // time window for NUMI beam flash, (us) 
            numiTimeMax: 18,
            peScaling: 1,
        };
        this.camera = {
            depth: 2000
        }
    }

    nTPC() { return this.tpc.location.length; }
    
    halfXYZ(i) { return [
        (this.tpc.location[i][1] - this.tpc.location[i][0]) / 2,
        (this.tpc.location[i][3] - this.tpc.location[i][2]) / 2,
        (this.tpc.location[i][5] - this.tpc.location[i][4]) / 2]; 
    }

    center(i) { return [
        (this.tpc.location[i][1] + this.tpc.location[i][0]) / 2,
        (this.tpc.location[i][3] + this.tpc.location[i][2]) / 2,
        (this.tpc.location[i][5] + this.tpc.location[i][4]) / 2]; 
    }

    driftDir(i) {
        return ((i % 2) -0.5) * -2; // -1 or 1
    }

    // Drift velocity (cm/us) for TPC iTPC.  Base: the experiment's single
    // tpc.driftVelocity — correct for detectors whose drift volumes share one
    // calibrated speed.  Detectors calibrated per volume override this,
    // mirroring driftDir/flashTimeForTPC.
    driftVelocityForTPC(iTPC) {
        return this.tpc.driftVelocity;
    }

    // Flash time (us) to use when drift-shifting TPC iTPC's boxes/PDs/charge
    // for the current flash.  Base: the op dump's single op_t — correct for
    // detectors whose charge crates share one readout clock.  Detectors whose
    // crates frame readout windows independently (PDVD BDE/TDE) override this
    // to pick the per-side clock (op_t1) for the second side.
    flashTimeForTPC(op, iTPC) {
        return op.data.op_t[op.currentFlash];
    }

    // Whether the U/V/W projection view (index 0/1/2) must be horizontally
    // mirrored to match the signal-processing image.
    //
    // SP plots horizontal = channel number. The channel->wire mapping in the WCT
    // wire-geometry files does NOT run uniformly along global +Z; its direction
    // depends on detector/plane/readout-face. Bee always renders the projection
    // horizontal along +pitch (~ +Z). Where the SP channel direction disagrees
    // with Bee's +Z, the images are horizontal mirrors. Default: no mirror.
    // (See docs / channel-vs-Z table; SBND numbers channels consistently +Z so
    // it needs no mirror.)
    projMirror(index, reverseDrift) { return false; }

    // Resolve a U/V/W projection (index 0/1/2) into the scene rotation angle (deg)
    // and whether to horizontally mirror it, to match the SP image. The default
    // uses viewAngle[index] (negated for reverse drift) and projMirror(). Detectors
    // whose readout-face wire geometry differs more substantially (e.g. PDVD, whose
    // reverse anodes swap U/V wire tilts) override this.
    projView(index, reverseDrift) {
        let angleDeg = this.tpc.viewAngle[index];
        if (reverseDrift) angleDeg = -angleDeg;
        return { angleDeg, mirror: this.projMirror(index, reverseDrift) };
    }

    opTPC(i) { return 0; } // TPC no for op detector i

    toString() { return `${this.name}`; }

    updateTPCLocation(loc) {
        this.tpc.location = loc;
        this.updateDimensions(0, this.nTPC() - 1);
    }

    updateBoxROI(loc) {
        this.tpc.boxROI = loc;
    }

    updateDimensions(first, last) {
        this.tpc.halfxyz[0] = (this.tpc.location[last][1] - this.tpc.location[first][0]) / 2;
        this.tpc.halfxyz[1] = (this.tpc.location[last][3] - this.tpc.location[first][2]) / 2;
        this.tpc.halfxyz[2] = (this.tpc.location[last][5] - this.tpc.location[first][4]) / 2;
        this.tpc.center[0] = (this.tpc.location[last][1] + this.tpc.location[first][0]) / 2;
        this.tpc.center[1] = (this.tpc.location[last][3] + this.tpc.location[first][2]) / 2;
        this.tpc.center[2] = (this.tpc.location[last][5] + this.tpc.location[first][4]) / 2;
    }

    updateOPLocation(loc, size) {
        this.op.location = loc;
        this.op.nDet = size;
    }

    toLocalXYZ(x, y, z) { // global (larsoft) coordinate to local (bee) coordinate
        return [
            x - this.tpc.center[0],
            y - this.tpc.center[1],
            z - this.tpc.center[2],
        ];
    }

    toGlobalXYZ(x, y, z) { // local (bee) coordinate to global (larsoft) coordinate
        return [
            x + this.tpc.center[0],
            y + this.tpc.center[1],
            z + this.tpc.center[2],
        ];
    }

    // index of the TPC whose active volume box contains the global (larsoft) point;
    // on no containment, fall back to the TPC whose center is nearest in x.
    // Used by the detector-frame view to apply the exact dual of the op.js box shift.
    tpcOf(gx, gy, gz) {
        for (let i = 0; i < this.nTPC(); i++) {
            let l = this.tpc.location[i];
            if (gx >= l[0] && gx <= l[1] &&
                gy >= l[2] && gy <= l[3] &&
                gz >= l[4] && gz <= l[5]) {
                return i;
            }
        }
        let best = 0, bestDx = Infinity;
        for (let i = 0; i < this.nTPC(); i++) {
            let dx = Math.abs(gx - this.center(i)[0]);
            if (dx < bestDx) { bestDx = dx; best = i; }
        }
        return best;
    }

    // Optional per-cluster {tpc, t} override for the detector-frame T0 correction,
    // keyed by cluster_id. null (the default) means use the baseline: drift direction
    // from the matched flash's apa (clusterTpcMap) and the current flash's time.
    // ProtoDUNEHD overrides this -- see its detectorFrameCorrection.
    detectorFrameCorrection(sst, op) { return null; }

}


